// ---------------------------------------------------------------------------
//  Build a slang context string for AI translation prompts.
//  Handles direct lookups, reverse lookups, and cross-language pairs
//  (e.g. ja→ko goes through ja→en + en→ko).
// ---------------------------------------------------------------------------
export function buildSlangContext(sourceLang, targetLang) {
  const directKey = `${sourceLang}-${targetLang}`

  // Direct dictionary exists (e.g. en-ja, ja-en, ko-en)
  if (SLANG_DICTS[directKey]) {
    return Object.entries(SLANG_DICTS[directKey])
      .map(([k, v]) => `- ${k} = ${v}`)
      .join('\n')
  }

  // Reverse lookup: flip an existing dict (e.g. fr-en request but only en-fr exists)
  const reverseKey = `${targetLang}-${sourceLang}`
  if (SLANG_DICTS[reverseKey]) {
    return Object.entries(SLANG_DICTS[reverseKey])
      .map(([v, k]) => `- ${k} = ${v}`)
      .join('\n')
  }

  // Cross-language: source→en + en→target (e.g. ja→ko)
  const sourceToEn = SLANG_DICTS[`${sourceLang}-en`]
  const enToTarget = SLANG_DICTS[`en-${targetLang}`]
  if (sourceToEn && enToTarget) {
    const lines = []
    for (const [native, en] of Object.entries(sourceToEn)) {
      // en value may contain " / " alternatives; try first token
      const enKey = en.split(' / ')[0].split(' (')[0].trim().toLowerCase()
      // Find matching entry in en→target (case-insensitive)
      const targetEntry = Object.entries(enToTarget).find(
        ([k]) => k.toLowerCase() === enKey
      )
      if (targetEntry) {
        lines.push(`- ${native} = ${targetEntry[1]}`)
      }
    }
    if (lines.length > 0) return lines.join('\n')
  }

  return ''
}

